//Simple if
var x=10;
if(x==5)
    {
        console.log(x)
    }

//If else
var x=10;
if(x==5)
    {
        console.log(x)
    }
else
    {
    console.log('fail')
    }

//Nested if
var x=10,
y=2;
if(x==10)
    {
        if(x>y)
            {
                console.log('pass')

            }
    }

//if else if ladder
var x=10,
y=11;
if(x==5)
    {
        console.log('pass')
    }
else if(y==11)
    {
        console.log('fail')
    }

//switch

var x=3
switch (x)
{
    case 1:console.log('case 1')
    break;
    case 2:console.log('case 2')
    break;
    case 3:console.log('case 3')
    break;
    default:console.log('invalid')
}

