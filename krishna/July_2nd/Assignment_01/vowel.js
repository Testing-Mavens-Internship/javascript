let letter='a';
switch (letter)
{
    case 'a':console.log('a is a vowel')
    break;
    case 'e':console.log('e is a vowel')
    break;
    case 'i':console.log('i is a vowel')
    break;
    case 'o':console.log('o is a vowel')
    break;
    case 'u':console.log('u is a vowel')
    break;
    default:console.log('Not a vowel')
}