let mark = 31
if ((100 >= mark) && (mark > 90)) {
    console.log(`Mark ${mark} = A Grade`);
} else if ((90 >= mark) && (mark > 80)) {
    console.log(`Mark ${mark} = B Grade`);

} else if ((80 >= mark) && (mark > 70)) {
    console.log(`Mark ${mark} = C Grade`);

} else if ((70 >= mark) && (mark > 60)) {
    console.log(`Mark ${mark} = D Grade`);
}
else {
    console.log(`Mark ${mark} = E Grade`);
}